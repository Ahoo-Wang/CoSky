package me.ahoo.cosky.rest.config;

import me.ahoo.cosky.rest.security.AuthorizeHandlerInterceptor;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import springfox.documentation.builders.PathSelectors;
import springfox.documentation.builders.RequestHandlerSelectors;
import springfox.documentation.service.*;
import springfox.documentation.spi.DocumentationType;
import springfox.documentation.spi.service.contexts.SecurityContext;
import springfox.documentation.spring.web.plugins.Docket;

import java.util.Arrays;
import java.util.Collections;
import java.util.List;

/**
 * @author ahoo wang
 */
@Configuration
public class SwaggerConfig {
    @Bean
    public Docket api() {
        return new Docket(DocumentationType.OAS_30)
                .apiInfo(apiInfo())
                .securityContexts(Collections.singletonList(securityContext()))
                .securitySchemes(Collections.singletonList(apiKey()))
                .select()
                .apis(RequestHandlerSelectors.any())
                .paths(PathSelectors.any())
                .build();
    }

    private ApiInfo apiInfo() {
        return new ApiInfo(
                "CoSky REST API",
                "High-performance, low-cost microservice governance platform. Service Discovery and Configuration Service.",
                "1.0",
                "https://github.com/Ahoo-Wang",
                new Contact("ahoo wang", "https://github.com/Ahoo-Wang", "ahoowang@qq.com"),
                "Apache-2.0",
                "https://github.com/Ahoo-Wang/CoSky/blob/main/LICENSE",
                Collections.emptyList());
    }

    private SecurityContext securityContext() {
        return SecurityContext.builder().securityReferences(defaultAuth()).build();
    }

    private List<SecurityReference> defaultAuth() {
        AuthorizationScope authorizationScope = new AuthorizationScope("global", "");
        AuthorizationScope[] authorizationScopes = new AuthorizationScope[1];
        authorizationScopes[0] = authorizationScope;
        return Collections.singletonList(new SecurityReference(AuthorizeHandlerInterceptor.AUTH_HEADER, authorizationScopes));
    }

    private ApiKey apiKey() {
        return new ApiKey(AuthorizeHandlerInterceptor.AUTH_HEADER, AuthorizeHandlerInterceptor.AUTH_HEADER, "header");
    }
}
